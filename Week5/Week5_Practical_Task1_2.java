package Week5;

public class Week5_Practical_Task1_2 {

    public static void main(String[] args) {

        System.out.println("Welcome to a program where I am printing numbers from 10 to 50:");

        //While loop to print numbers 10 to 50.
        int i = 10;
        while (i<=50) {
            System.out.println(i);
            i++;
        }
    
        System.out.println("This is the end of the program:");

    }
    
}