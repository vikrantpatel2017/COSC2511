package Week5;

public class Week5_Practical_Task1_3 {
    
    public static void main(String[] args) {
        System.out.println("Welcome to the program which will print numbers from 100 to 1 using do while loop!");

        int i = 100;
        do {
            System.out.println(i);
            i--;
        } while (i>=1);


    }


}
